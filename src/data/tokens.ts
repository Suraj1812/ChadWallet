export function enrichToken(token: Partial<Token>, index: number): Token {
  const fallback = fallbackTokens[index % fallbackTokens.length];
  const price = Number.isFinite(token.price) && token.price ? token.price : fallback.price;

  return {
    ...fallback,
    ...token,
    address: token.address || fallback.address,
    symbol: token.symbol || fallback.symbol,
    name: token.name || fallback.name,
    price,
    rank: index + 1,
    accent: token.accent || fallback.accent,
    sparkline: token.sparkline?.length ? token.sparkline : fallback.sparkline,
    holders: token.holders?.length ? token.holders : fallback.holders,
    trades: token.trades?.length ? token.trades : fallback.trades,
  };
}
